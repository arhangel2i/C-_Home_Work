void PrintArray(int[,] mat)
{
    for (int i = 0; i < mat.GetLength(0); i++)
    {
        for (int j = 0; j < mat.GetLength(1); j++)
        {
            Console.Write($"{mat[i,j]} ");
        }
        Console.WriteLine();
    }
}

void PrinImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i,j]==1) Console.Write("+");
            else Console.Write(" ");
        }
        Console.WriteLine();
    }
}

void FillArray(int[,] matf)
{
    for (int i = 0; i < matf.GetLength(0); i++)
    {
        for (int j = 0; j < matf.GetLength(1); j++)
        {
           matf[i,j] = new Random().Next(1,10);
        }
    }
}

void FillImage(int[,] pic, int row, int col)
{
    if (pic[row, col]==0)
    {
        pic[row,col]=1;
        FillImage(pic, row-1, col);
        FillImage(pic, row, col-1);
        FillImage(pic, row+1, col);
        FillImage(pic, row, col+1);
    }
}

